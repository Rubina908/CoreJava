package DataType;

public class PrimitiveDataType {
	
	/*primitive data Type:
	 * 1. int
	 * 2.float
	 * 3. double
	 * 4.char
	 * 5. boolean
	 */
public static void main(String[] args) {
	
	//primitive data type is know as call by value
	
	//integer
	int a ;//declaration
	a=30;//initalization
	System.out.println(a);
	
	//double

	double b;
	b=12.3;
	System.out.println(b);
	
	//boolean
	boolean my;
	my=true;
	System.out.println("boolean return true or false");
	
	
	//char
	char c;
	c='t';
	System.out.println("char will be enclose in single code");
	
	
	//float
	float f;
	f=3;
	System.out.println(f);

}
	
	
	
}
